// 02_Variables.js
// 변수, Variables

// Number
console.log("---Number---")
let number = 1;
console.log(number);
number = 2;
console.log(number);
/* output
1
2
*/

// String
console.log("---String---")
let string = "Hello, ";
console.log(string);
string = "JavaScript!";
console.log(string);
/* output
1
2
*/

// Boolean
console.log("---Boolean---")
let boolean = true;
console.log(boolean);
boolean = false;
console.log(boolean);
/* output
true
false
*/

// null, 변수를 선언하였고 null(없음을 의미) 값을 가지고 있는 상태
console.log("---null---")
let Null = null;
console.log(Null);
/* output
null
*/

// undefined, 변수를 선언하였지만 값을 지정하지 않은 상태
console.log("---undefined---")
let Undefined;
console.log(Undefined);
/* output
undefined
*/

// 상수, Constants (Read-Only)
console.log("---constant---")
const a = 1;
/* output
true
false
*/
