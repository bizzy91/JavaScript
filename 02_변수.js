// 변수, Variables

// Number
let number = 1;
console.log(number);
number = 2;
console.log(number);
// String
let string = "Hello, ";
console.log(string);
string = "JavaScript!";
console.log(string);
// Boolean
let boolean = true;
console.log(boolean);
boolean = false;
console.log(boolean);
